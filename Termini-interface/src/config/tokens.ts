export const initConfig = async () => {
  const response = await fetch(DEFAULT_TOKEN_CONFIG_URL);
  const jsonData = await response.json();
  if (jsonData) {
    jsonData.TOKENS?.length && jsonData.TOKENS.forEach((item: any) => {
      if (jsonData.isV2Available) {
        TRADE_TOKENS_IDS_V2_NEW[item.pricesIdsName] = item.pricesIds;
      } else {
        TRADE_TOKENS_IDS_V1_NEW[item.pricesIdsName] = item.pricesIds;
      }
      K_LINE_SYMBOLNAME_MAP_NEW[item.pricesIdsName]= item.K_LINE_SYMBOLNAME;
      if(TOKENS_NEW.findIndex(i => i.pricesIdsName === item.pricesIdsName) === -1) {
        TOKENS_NEW.push(item);
      }
    })
  }
}
